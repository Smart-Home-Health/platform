// Global test setup, loaded by Vitest before each test file.
import '@testing-library/jest-dom';
import { afterEach } from 'vitest';
import { cleanup } from '@testing-library/react';

// Unmount React trees between tests so DOM/state never leaks across cases.
afterEach(() => {
  cleanup();
});

// jsdom doesn't implement matchMedia; provide a no-op default so contexts that
// read it (ThemeContext) don't crash. Individual tests may override it.
if (typeof window !== 'undefined' && !window.matchMedia) {
  window.matchMedia = (query) => ({
    matches: false,
    media: query,
    onchange: null,
    addEventListener: () => {},
    removeEventListener: () => {},
    addListener: () => {},
    removeListener: () => {},
    dispatchEvent: () => false,
  });
}
